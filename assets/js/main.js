var database = firebase.database();

// Function to fetch sensor data
function getSensorData() {
  // Reference to the path where your sensor data is stored
  var sensorRef = database.ref('sensorData'); // Replace with your actual path

  // Read the sensor data from Firebase
  sensorRef.on('value', function(snapshot) {
    var sensorData = snapshot.val(); // The value of the data from Firebase

    // Display the sensor data on the webpage
    document.getElementById('sensor-data').innerHTML = "Sensor Data: " + sensorData;
  });
}

// Call the function to get sensor data when the page loads
window.onload = function() {
  getSensorData();
};